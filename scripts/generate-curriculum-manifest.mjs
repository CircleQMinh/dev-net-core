const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const projectRoot = path.resolve(scriptDir, "..");
const contentRoot = path.join(projectRoot, "src", "contents", "v1");
const outputPath = path.join(
  projectRoot,
  "src",
  "contents",
  "curriculumManifest.generated.ts"
);
const outputRelativePath = toPosix(path.relative(projectRoot, outputPath));
const v1ContentRoot = "/src/contents/v1/";

const specialLabels = {
  api: "API",
  aspnet: "ASP.NET",
  cqrs: "CQRS",
  csharp: "C#",
  ddd: "DDD",
  di: "DI",
  dns: "DNS",
  dotnet: ".NET",
  ef: "EF",
  grpc: "gRPC",
  id: "ID",
  ioc: "IoC",
  iqueryable: "IQueryable",
  jwt: "JWT",
  linq: "LINQ",
  md: "MD",
  mvc: "MVC",
  net: ".NET",
  nhibernate: "NHibernate",
  oop: "OOP",
  sql: "SQL",
  tpl: "TPL",
  tempdata: "TempData",
  viewbag: "ViewBag",
  viewdata: "ViewData",
  viewmodel: "ViewModel",
  vs: "vs",
  webapi: "WebAPI",
};

const markdownFiles = collectMarkdownFiles(contentRoot)
  .map((filePath) => ({
    absolutePath: filePath,
    relativePath: toPosix(path.relative(contentRoot, filePath)),
  }))
  .sort((left, right) => left.relativePath.localeCompare(right.relativePath));

const usedTopicIds = new Set();
const entries = markdownFiles.map(({ absolutePath, relativePath }) => {
  const markdown = fs.readFileSync(absolutePath, "utf8");
  const segments = relativePath.split("/");
  const markdownFileName = segments.at(-1);

  if (!markdownFileName) {
    throw new Error(`Could not resolve markdown file name for ${absolutePath}`);
  }

  const topicPath = relativePath.replace(/\.md$/i, "");
  const parentFolderName = segments.at(-2) ?? "v1";
  const frontmatter = parseMarkdownFrontmatter(markdown);
  const category =
    frontmatter.category?.trim() || formatSegmentLabel(parentFolderName);
  const topicTitle =
    frontmatter.topic?.trim() || formatSegmentLabel(parentFolderName);
  const subtopicTitle =
    frontmatter.subtopic?.trim() ||
    frontmatter.topic?.trim() ||
    formatSegmentLabel(markdownFileName.replace(/\.md$/i, ""));
  const fallbackId = slugify(topicPath);
  const id = getUniqueTopicId(
    frontmatter.id?.trim() || fallbackId,
    fallbackId,
    usedTopicIds,
    relativePath
  );
  const physicalFolderSegments = segments.slice(0, -1);
  const folders = physicalFolderSegments.map((folderSegment, index) => {
    const folderPath = physicalFolderSegments.slice(0, index + 1).join("/");

    return {
      path: folderPath,
      title: formatSegmentLabel(folderSegment),
    };
  });
  const physicalFolderPath = physicalFolderSegments.join("/");
  const topicFolderPath = [physicalFolderPath, slugify(topicTitle)]
    .filter(Boolean)
    .join("/");
  const questions = extractCommonInterviewQuestions(markdown);
  const questionLevelCounts = getQuestionLevelCounts(questions);

  return {
    category,
    contentPath: `${v1ContentRoot}${relativePath}`,
    folders,
    id,
    path: topicPath,
    questionCount: questionLevelCounts.reduce(
      (total, levelCount) => total + levelCount.count,
      0
    ),
    questionLevelCounts,
    sampleQuestions: getSampleQuestions(questions),
    subtopic: subtopicTitle,
    title: subtopicTitle,
    topic: topicTitle,
    topicFolderPath,
  };
});

