function collectMarkdownFiles(directory) {
  if (!fs.existsSync(directory)) {
    return [];
  }

  return fs.readdirSync(directory, { withFileTypes: true }).flatMap((entry) => {
    const entryPath = path.join(directory, entry.name);

    if (entry.isDirectory()) {
      return collectMarkdownFiles(entryPath);
    }

    return entry.isFile() && entry.name.toLowerCase().endsWith(".md")
      ? [entryPath]
      : [];
  });
}

function parseMarkdownFrontmatter(markdown) {
  const frontmatterMatch = markdown.match(/^---\r?\n([\s\S]*?)\r?\n---/);
  const frontmatterBody = frontmatterMatch?.[1];

  if (!frontmatterBody) {
    return {};
  }

  return frontmatterBody.split(/\r?\n/).reduce((frontmatter, line) => {
    const separatorIndex = line.indexOf(":");

    if (separatorIndex === -1) {
      return frontmatter;
    }

    const key = line.slice(0, separatorIndex).trim();
    const value = stripWrappingQuotes(line.slice(separatorIndex + 1).trim());

    if (
      key === "id" ||
      key === "topic" ||
      key === "subtopic" ||
      key === "category"
    ) {
      frontmatter[key] = value;
    }

    return frontmatter;
  }, {});
}

function getQuestionLevelCounts(questions) {
  const countsByLevel = new Map();

  questions.forEach((question) => {
    countsByLevel.set(question.level, (countsByLevel.get(question.level) ?? 0) + 1);
  });

  return Array.from(countsByLevel.entries()).map(([level, count]) => ({
    count,
    level,
  }));
}

function getSampleQuestions(questions) {
  const question =
    questions.find((item) => item.level === "Intermediate") ??
    questions.find((item) => item.level === "Advanced") ??
    questions.find((item) => item.level === "Beginner") ??
    questions[0];

  if (!question) {
    return [];
  }

  return [
    {
      answerExcerpt: createAnswerExcerpt(question),
      id: question.id,
      label: question.label,
      level: question.level,
      question: question.question,
    },
  ];
}

function createAnswerExcerpt(question) {
  const excerptSource =
    question.expectedAnswerMarkdown ||
    question.keyPointsMarkdown ||
    question.question;
  const excerpt = truncateText(markdownToPlainText(excerptSource), 220);

  return (
    excerpt ||
    "Practice this question and compare your answer with the expected response."
  );
}

function markdownToPlainText(markdown) {
  return removeCommentFromMD(markdown)
    .replace(/```[\s\S]*?```/g, " ")
    .replace(/`([^`]+)`/g, "$1")
    .replace(/!\[([^\]]*)\]\([^)]+\)/g, "$1")
    .replace(/\[([^\]]+)\]\([^)]+\)/g, "$1")
    .replace(/^#{1,6}\s+/gm, "")
    .replace(/^[-*+]\s+/gm, "")
    .replace(/^\d+\.\s+/gm, "")
    .replace(/[*_~>#|]/g, " ")
    .replace(/\s+/g, " ")
    .trim();
}

function truncateText(text, maxLength) {
  if (text.length <= maxLength) {
    return text;
  }

  const truncatedText = text.slice(0, maxLength).trimEnd();
  const lastSpaceIndex = truncatedText.lastIndexOf(" ");

  return `${truncatedText.slice(0, Math.max(0, lastSpaceIndex)).trimEnd()}...`;
}

function extractCommonInterviewQuestions(markdown) {
  const section = extractCommonInterviewQuestionsSection(markdown);

  if (!section) {
    return [];
  }

  const { eol, lines } = splitMarkdownLines(section);
  const questions = [];
  let currentLevel = "";
  let isInCodeBlock = false;

  lines.forEach((line, index) => {
    if (/^\s{0,3}(```|~~~)/.test(line)) {
      isInCodeBlock = !isInCodeBlock;
      return;
    }

    if (isInCodeBlock) {
      return;
    }

    const heading = getMarkdownHeadingMatch(line);

    if (!heading) {
      return;
    }

    if (heading.level === 3) {
      currentLevel = heading.title;
      return;
    }

    if (heading.level !== 4) {
      return;
    }

    const nextQuestionIndex = findNextQuestionBoundary(lines, index + 1);
    const questionMarkdown = lines.slice(index, nextQuestionIndex).join(eol).trim();
    const parsedHeading = parseQuestionHeading(heading.title, currentLevel);
    const questionId =
      questionMarkdown.match(/<!--\s*question-id:([\w./:-]+)\s*-->/i)?.[1] ??
      slugify(heading.title);
    const questionLevel =
      questionMarkdown.match(/<!--\s*question-level:([\w -]+)\s*-->/i)?.[1] ??
      parsedHeading.level;

    questions.push({
      expectedAnswerMarkdown: extractMarkdownSectionBody(
        questionMarkdown,
        "Expected Answer"
      ),
      id: questionId.trim(),
      keyPointsMarkdown: extractMarkdownSectionBody(
        questionMarkdown,
        "Key Points to Mention"
      ),
      label: parsedHeading.label,
      level: formatQuestionLevel(questionLevel),
      question: parsedHeading.question,
    });
  });

  return questions;
}

function extractMarkdownSectionBody(markdown, headingTitle) {
  const sectionRange = findMarkdownSectionRange(markdown, headingTitle);

  if (!sectionRange) {
    return "";
  }

  const { eol, lines } = splitMarkdownLines(markdown);

  return removeCommentFromMD(
    lines
      .slice(sectionRange.startLine + 1, sectionRange.endLine)
      .join(eol)
      .trim()
  );
}

function removeCommentFromMD(markdown) {
  return markdown.replace(/<!--[\s\S]*?-->/g, "");
}

function extractCommonInterviewQuestionsSection(markdown) {
  const sectionRange = findMarkdownSectionRange(
    markdown,
    "Common Interview Questions"
  );

  if (!sectionRange) {
    return "";
  }

  const { eol, lines } = splitMarkdownLines(markdown);

  return lines
    .slice(sectionRange.startLine, sectionRange.endLine)
    .join(eol)
    .trim();
}

function findMarkdownSectionRange(markdown, headingTitle) {
  const { lines } = splitMarkdownLines(markdown);
  const normalizedTargetTitle = normalizeMarkdownHeadingTitle(headingTitle);
  let sectionStartLine = -1;
  let sectionHeadingLevel = 0;
  let isInCodeBlock = false;

  for (let index = 0; index < lines.length; index += 1) {
    const line = lines[index];

    if (/^\s{0,3}(```|~~~)/.test(line)) {
      isInCodeBlock = !isInCodeBlock;
      continue;
    }

    if (isInCodeBlock || /^(?: {4}|\t)/.test(line)) {
      continue;
    }

    const heading = getMarkdownHeadingMatch(line);

    if (!heading) {
      continue;
    }

    if (sectionStartLine === -1) {
      if (normalizeMarkdownHeadingTitle(heading.title) === normalizedTargetTitle) {
        sectionStartLine = index;
        sectionHeadingLevel = heading.level;
      }

      continue;
    }

    if (heading.level <= sectionHeadingLevel) {
      return {
        endLine: index,
        headingLevel: sectionHeadingLevel,
        startLine: sectionStartLine,
      };
    }
  }

  if (sectionStartLine === -1) {
    return undefined;
  }

  return {
    endLine: lines.length,
    headingLevel: sectionHeadingLevel,
    startLine: sectionStartLine,
  };
}

function findNextQuestionBoundary(lines, startIndex) {
  let isInCodeBlock = false;

  for (let index = startIndex; index < lines.length; index += 1) {
    const line = lines[index];

    if (/^\s{0,3}(```|~~~)/.test(line)) {
      isInCodeBlock = !isInCodeBlock;
      continue;
    }

    if (isInCodeBlock) {
      continue;
    }

    const heading = getMarkdownHeadingMatch(line);

    if (heading && heading.level <= 4) {
      return index;
    }
  }

  return lines.length;
}

function getMarkdownHeadingMatch(line) {
  const headingMatch = line.match(/^(#{1,6})\s+(.+?)\s*#*\s*$/);

  if (!headingMatch) {
    return undefined;
  }

  const title = formatMarkdownHeadingText(headingMatch[2]);

  if (!title) {
    return undefined;
  }

  return {
    level: headingMatch[1].length,
    title,
  };
}

function parseQuestionHeading(title, fallbackLevel) {
  const headingMatch = title.match(
    /^(?:(beginner|intermediate|advanced)\s+)?(q\d+)\s*:\s*(.+)$/i
  );

  if (!headingMatch) {
    return {
      label: "",
      level: fallbackLevel,
      question: title,
    };
  }

  return {
    label: headingMatch[2].toUpperCase(),
    level: headingMatch[1] ?? fallbackLevel,
    question: headingMatch[3].trim(),
  };
}

function formatQuestionLevel(level) {
  const normalizedLevel = level.trim().toLowerCase();

  if (!normalizedLevel) {
    return "General";
  }

  return `${normalizedLevel.charAt(0).toUpperCase()}${normalizedLevel.slice(1)}`;
}

function normalizeMarkdownHeadingTitle(title) {
  return formatMarkdownHeadingText(title).toLowerCase();
}

function formatMarkdownHeadingText(text) {
  return text
    .replace(/<[^>]+>/g, "")
    .replace(/!\[([^\]]*)\]\([^)]+\)/g, "$1")
    .replace(/\[([^\]]+)\]\([^)]+\)/g, "$1")
    .replace(/[`*_~]/g, "")
    .trim();
}

function splitMarkdownLines(markdown) {
  return {
    eol: markdown.includes("\r\n") ? "\r\n" : "\n",
    lines: markdown.split(/\r?\n/),
  };
}

function getUniqueTopicId(requestedId, fallbackId, usedTopicIds, contentPath) {
  if (!usedTopicIds.has(requestedId)) {
    usedTopicIds.add(requestedId);
    return requestedId;
  }

  let duplicateIndex = 2;
  let uniqueId = `${requestedId}--${fallbackId}`;

  while (usedTopicIds.has(uniqueId)) {
    uniqueId = `${requestedId}--${fallbackId}-${duplicateIndex}`;
    duplicateIndex += 1;
  }

  usedTopicIds.add(uniqueId);
  console.warn(
    `Duplicate curriculum topic id "${requestedId}" found in v1 content. ` +
      `Using "${uniqueId}" for ${contentPath}.`
  );

  return uniqueId;
}

function slugify(value) {
  return (
    value
      .replace(/\\/g, "/")
      .replace(/\.md$/i, "")
      .toLowerCase()
      .replace(/[^a-z0-9]+/g, "-")
      .replace(/^-+|-+$/g, "") || "topic"
  );
}

function formatSegmentLabel(segment) {
  return segment
    .replace(/\.md$/i, "")
    .replace(/([a-z])(\d)/gi, "$1 $2")
    .split(/[-_\s.]+/)
    .filter(Boolean)
    .map((word) => {
      const lowerWord = word.toLowerCase();

      if (specialLabels[lowerWord]) {
        return specialLabels[lowerWord];
      }

      return `${word.charAt(0).toUpperCase()}${word.slice(1).toLowerCase()}`;
    })
    .join(" ");
}

function stripWrappingQuotes(value) {
  const firstCharacter = value.at(0);
  const lastCharacter = value.at(-1);

  if (
    value.length >= 2 &&
    ((firstCharacter === `"` && lastCharacter === `"`) ||
      (firstCharacter === "'" && lastCharacter === "'"))
  ) {
    return value.slice(1, -1);
  }

  return value;
}

function toPosix(filePath) {
  return filePath.split(path.sep).join("/");
}
